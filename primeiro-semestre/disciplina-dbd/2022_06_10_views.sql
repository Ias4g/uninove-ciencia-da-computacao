-- SQL - VIEWs

CREATE OR REPLACE VIEW VTESTE AS
SELECT    EMPNO, ENAME, JOB, SAL
FROM      EMP;


-- VISUALIZANDO UMA VIEW
SELECT * FROM VTESTE;

SELECT ENAME, SAL
FROM VTESTE
WHERE SAL > 2000;


-- CRIANDO NOVA VIEW
CREATE OR REPLACE VIEW VTESTE1
AS
SELECT *
FROM EMP
WHERE DEPTNO = 20;

SELECT * FROM VTESTE1;

-- JOIN COM VIEW
SELECT ENAME, JOB, SAL, DNAME
FROM VTESTE1 INNER JOIN DEPT
ON VTESTE1.DEPTNO = DEPT.DEPTNO;








